    public listarEmpleados() {
        initComponents();
        try{
            System.setProperty("java.rmi.server.hostname", 
                    "192.168.207.106");
            DAOEmployee daoEmployee = (DAOEmployee)java.rmi.Naming.lookup("//192.168.207.105:1234/daoEmployee");
            DAODepartment daoDepartment = (DAODepartment)java.rmi.Naming.lookup("//192.168.207.105:1234/daoDepartment");
            
            
            DefaultMutableTreeNode raiz = new DefaultMutableTreeNode("Empleados");
            depsInd = daoDepartment.queryAllIndependent();
            
            for(Department d :  depsInd){
                DefaultMutableTreeNode nuevoDI = new DefaultMutableTreeNode(d);
                
                depsDep= daoDepartment.queryAllDependent(d.getId());
                for(Department dd: depsDep){
                    DefaultMutableTreeNode nuevoDD = new DefaultMutableTreeNode(dd);
                    empsInd = daoEmployee.queryAllIndependent(dd.getId());
                    for(Employee ei : empsInd ){
                        DefaultMutableTreeNode nuevoEI = new DefaultMutableTreeNode(ei);
                        empsDep = daoEmployee.queryAllDependent(ei.getId());
                        for(Employee ed : empsDep){
                            DefaultMutableTreeNode nuevoED = new DefaultMutableTreeNode(ed);
                            nuevoEI.add(nuevoED);
                        }
                        nuevoDD.add(nuevoEI);
                    }
                    nuevoDI.add(nuevoDD);
                }
                empsInd = daoEmployee.queryAllIndependent(d.getId());
                for(Employee ei : empsInd ){
                    DefaultMutableTreeNode nuevoEI = new DefaultMutableTreeNode(ei);
                    empsDep = daoEmployee.queryAllDependent(ei.getId());
                    for(Employee ed : empsDep){
                        DefaultMutableTreeNode nuevoED = new DefaultMutableTreeNode(ed);
                        nuevoEI.add(nuevoED);
                    }
                    nuevoDI.add(nuevoEI);
                }
                raiz.add(nuevoDI);
                
            }
            DefaultTreeModel modelo = new DefaultTreeModel(raiz);
            treeEmpleados.setModel(modelo);
            
        }catch(Exception ex){
            System.err.println(ex.getMessage());
        }
        
        
        
        
    }
    
    
    

