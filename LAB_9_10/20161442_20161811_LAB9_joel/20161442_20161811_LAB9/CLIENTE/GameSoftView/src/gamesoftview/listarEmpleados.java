    private void treeEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_treeEmpleadosMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount()==2){
            DefaultMutableTreeNode nodo = (DefaultMutableTreeNode) treeEmpleados.getLastSelectedPathComponent();
            Employee e =  (Employee)nodo.getUserObject();
            lblDNI.setText(e.getDNI());
            lblName.setText(e.getName() + " " + e.getLastName() + " " + e.getSecondLastName());
            lblRol.setText(e.getRole().getName());
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            
            lblBirth.setText(format.format(e.getBirthday()));
            lblEmail.setText(e.getEmail());
            lblSalary.setText(String.format("%.2f", e.getSalary()));
            
        }
    }//GEN-LAST:event_treeEmpleadosMouseClicked

